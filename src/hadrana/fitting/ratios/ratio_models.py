def ratio_model_qmax_zero(model_id: str, t_tau, *params):
    t, tau = t_tau[:, 0], t_tau[:, 1]
    fit_model_id = f"FIT-ID-{model_id}-QMAX0"
    models = {
        'FIT-ID-A1-QMAX0': lambda M: np.full_like(t, M, dtype=np.float64),

        'FIT-ID-B3-QMAX0': lambda M, b1, dM1: 
            M * (1 + b1 * np.exp(-dM1 * t / 2) * np.cosh(dM1 * (tau - t / 2))),

        # 'FIT-ID-B3-QMAX0': lambda M, b1, dM1: 
        #     M * (1 + b1 * np.exp(-dM1 * t / 2) * np.cosh(dM1 * (tau - t / 2))),

        'FIT-ID-C4-QMAX0': lambda M, b1, b2, dM1: 
            M * (1 + b1 * np.exp(-dM1 * t / 2) * np.cosh(dM1 * (tau - t / 2))
                + b2 * np.exp(-dM1 * t)),

        # 'FIT-ID-C4-QMAX0': lambda S, b1, b2: 
        #     S * (1 + b1 * np.exp(-0.1 * t / 2) * np.cosh(0.1 * (tau - t / 2))
        #         + b2 * np.exp(-0.1 * t)),

        'FIT-ID-D5-QMAX0': lambda M, b1, b3, dM1:
            M * (1 + b1 * np.exp(-dM1 * t / 2) * np.cosh(dM1 * (tau - t / 2)) +
                 b3 * np.exp(-0.55 * tau) * np.exp(-0.55 * (t - tau))),
     }
    if fit_model_id not in models:
        raise ValueError(f"Unknown model_id: {fit_model_id}")

    return models[fit_model_id](*params)

def ratio_model_qmax_finite(qmax: int, model_id: str, t_tau, *params):
    t, tau = t_tau[:, 0], t_tau[:, 1]
    fit_model_id = f"FIT-ID-{model_id}-QMAX{qmax}"

    models = {
        f"FIT-ID-A1-QMAX{qmax}": lambda M: np.full_like(t, M, dtype=np.float64),

        f"FIT-ID-B3-QMAX{qmax}": lambda M, B01, dE1q:
            M * (1 + B01 * np.exp(-dE1q * tau)),

        f"FIT-ID-C3-QMAX{qmax}": lambda M, B10, dE10:
            M * (1 + B10 * np.exp(-dE10 * (t - tau))),

        # f"FIT-ID-C3-QMAX{qmax}": lambda S, B10:
        #     S * (1 + B10 * np.exp(-0.895 * (t - tau))),

        # f"FIT-ID-C3-QMAX{qmax}": lambda S, B01:
        #     S * (1 + B01 * np.exp(-0.895 * tau)),

        f"FIT-ID-D5-QMAX{qmax}": lambda M, B01, B10, dE1q, dE10:
            M * (1 + B01 * np.exp(-dE1q * tau) + B10 * np.exp(-dE10 * (t - tau))),

        f"FIT-ID-E7-QMAX{qmax}": lambda M, B01, B10, B02, dE1q, dE10, dE2q:
            M * (1 + B01 * np.exp(-dE1q * tau) + B10 * np.exp(-dE10 * (t - tau)) + B02 * np.exp(-dE2q * tau)),

        f"FIT-ID-F7-QMAX{qmax}": lambda M, B01, B10, B20, dE1q, dE10, dE20:
            M * (1 + B01 * np.exp(-dE1q * tau) + B10 * np.exp(-dE10 * (t - tau)) + B20 * np.exp(-dE20 *( t-tau ))),

        f"FIT-ID-G9-QMAX{qmax}": lambda M, B01, B10, B02, B20, dE1q, dE10, dE2q, dE20:
            M * (1 + B01 * np.exp(-dE1q * tau) + B10 * np.exp(-dE10 * (t - tau)) + B02 * np.exp(-dE2q * tau) + B20 * np.exp(-dE20 *( t-tau ))),
    }

    if fit_model_id not in models:
        raise ValueError(f"Unknown model_id: {fit_model_id}")

    return models[fit_model_id](*params)

